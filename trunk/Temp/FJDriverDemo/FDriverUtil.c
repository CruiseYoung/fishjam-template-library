void DumpDeviceFlags(USHORT nFlags)
{
	USHORT nCheckFlags = nFlags;
	TCHAR szBuf[100] = {0};

	INT nCount = _countof(szBuf);

	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_VERIFY_VOLUME, "|");

	//读写操作使用缓冲方式(系统复制缓冲区)访问用户模式数据
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_BUFFERED_IO, ("|"));	
	//一次只允许一个线程打开设备句柄
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_EXCLUSIVE, ("|"));
	//读写操作使用直接方式(内存描述符表)访问用户模式数据
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_DIRECT_IO, ("|"));
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_MAP_IO_BUFFER, "|");
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_DEVICE_HAS_NAME, "|");
	//设备对象正在初始化
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_DEVICE_INITIALIZING, ("|"));
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_SYSTEM_BOOT_PARTITION, ("|"));
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_LONG_TERM_REQUESTS, ("|"));
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_NEVER_LAST_DEVICE, ("|"));
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_SHUTDOWN_REGISTERED, ("|"));
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_BUS_ENUMERATED_DEVICE, ("|"));
	//必须在 PASSIVE_LEVEL 级上处理 IRP_MJ_PNP 请求
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_POWER_PAGABLE, ("|"));
	//设备上电期间需要大电流
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_POWER_INRUSH, ("|"));
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_LOW_PRIORITY_FILESYSTEM, ("|"));
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_SUPPORTS_TRANSACTIONS, ("|"));
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_FORCE_NEITHER_IO, ("|"));
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_VOLUME_DEVICE_OBJECT, ("|"));
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_SYSTEM_SYSTEM_PARTITION, ("|"));
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_SYSTEM_CRITICAL_PARTITION, ("|"));
	HANDLE_COMBINATION_VALUE_TO_STRING_A(szBuf, nCount, nCheckFlags, DO_DISALLOW_EXECUTE, ("|"));

	KdPrint(("DeviceFlags=0x%x(%s)\n", nFlags, szBuf));
}

